			<div class="p-qa-answerbox">
				<p class="p-qa-answer"><span class="p-qa-title-logo">A</span>成年後見制度は、判断能力が不十分になってから家庭裁判所で成年後見人を選任し、成年後見人が財産管理を行う制度です。<br>
				任意後見制度は、判断能力が不十分になる前に、後見人になって欲しい人(ご親族や司法書士等の専門職)との間であらかじめ契約をしておき、判断能力が不十分になった際に、任意後見監督人が選任され、任意後見人が財産管理を行う制度です。</p>
