			<div class="p-qa-answerbox">
				<p class="p-qa-answer"><span class="p-qa-title-logo">A</span>出来ません。判断能力が不十分な方の保護のため、判断能力が回復したり、死亡するまで続きます。たとえば、施設入所の費用のために、判断能力が不十分なお父様の不動産を売却するため、いったん後見制度を利用したとします。しかし、不動産の売却が終わった後も、その後見制度は続いていきます。</p>
