		<div class="p-inheritance-qa">
			<h3 class="p-qa-title"><span class="p-qa-title-logo">Q</span>法定後見制度・任意後見制度とは？</h3>
			<div class="p-qa-answerbox">
				<p class="p-qa-answer"><span class="p-qa-title-logo">A</span>判断能力が不十分になった方の財産管理をする制度になります。<br>
				後見制度は法定後見制度と任意後見制度の２種類があります。判断能力が不十分になってしまった時には、誰かが代わりにその方の財産管理をする必要が出てきます。たとえば、ご本人がすでに判断能力が不十分であれば、法定後見制度を使います。また、判断能力が不十分になる前にご自身が契約で、財産管理をしてもらう人をあらかじめ決めておける制度が任意後見制度です。まだ判断能力があるのであれば、任意後見制度を活用し、依頼したいと思う方と契約を結んでおかれるのもよいでしょう。</p>
			</div><!--/.p-qa-answerbox-->
